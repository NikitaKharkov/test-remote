class Toc extends LoggedRecord {
	
	/**
	 * Returns the edit page string
	 *
	 * @param void
	 * @return string
	 */
	public function getEditPage() {
		return "/admin/tocs.php?toc_id={$this->getPrimaryKey()}&amp;page_function=edit";
	}
	
}
