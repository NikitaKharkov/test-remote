<h2><a name="what">What is DynaMed CE?</a></h2>
<h3>Physicians and nurse practitioners using DynaMed to inform and support their clinical decisions may now earn Continuing Medical Education (CME) credits and Continuing Nursing Education (CNE) Contact Hours, respectively, for this activity. As stated in the American Medical Association (AMA) Physician's Recognition Award and credit system, "Internet point of care (PoC) CME describes structured, self-directed, online learning by physicians on topics relevant to their clinical practice. Learning for this activity is driven by a reflective process in which physicians must document their clinical question, the sources consulted and the application to practice. Physicians conducting structured online searches on clinical topics may claim a half (0.5) <i>AMA PRA Category 1 Credit</i>&trade; for documented completion (either at the point of care or later) of the three-step learning cycle." For physicians claiming Internet point of care CME, there is a limit of twenty (20) 
<i>AMA PRA Category 1 Credits</i>&trade; per year.</h3>

