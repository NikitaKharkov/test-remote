<h2><a name="accreditation">Accreditation Statements</a></h2>
<h3><b><i>AMA PRA Category 1 Credit</i>&trade;</b></h3>
<h3><u>Physician</u></h3>
<h3>This activity has been planned and implemented in accordance with the Essential Areas and policies of the Accreditation Council for Continuing Medical Education (ACCME) through the joint sponsorship of Tufts University School of Medicine (TUSM) and Tufts Health Care Institute (THCI). TUSM is accredited by the ACCME to provide continuing medical education for physicians.</h3>
<h3>TUSM designates this educational activity for a maximum of 20 <i>AMA PRA Category 1 Credits</i>&trade; per year. Physicians may claim a half (0.5) <i>AMA PRA Category 1 Credit</i>&trade; for conducting a single structured online search on a clinical topic. For each search there must be documented completion of a three-step learning cycle, as follows: 1) review original clinical question(s); 2) identify the relevant sources from among those consulted; and 3) describe the application of their findings to practice.</h3>
<h3>There is a limit on the amount of credit a physician can claim per year toward their AMA PRA for Internet point of care CME. That limit is twenty (20) <i>AMA PRA Category 1 Credits</i>&trade; per year (or 40 searches at 0.5 credit per search). Physicians should only claim credit commensurate with the extent of their participation in the activity.<br/> 
