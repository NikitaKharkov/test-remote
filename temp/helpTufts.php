<h2><a name="claim">How to claim CE Credit</a></h2>
<h3>Credits are recorded on the Tufts Online Credit Tracker, where users can view their searches, claim credits, generate new CE Certificates, and print CE Certificates. Specific instructions are presented here and also in the email sent to users.</h3>
<ul style="padding-left:4em; list-style:square">
<li>Go to <a target="_blank" href="http://cme.thci.org">http://cme.thci.org</a></li>
<li>First-time users will need to request their password for the Credit Tracker (note: this is a different password than the user's DynaMed password). Click on "Forgot password" and enter the same email address entered on the DynaMed screen. Once in the site, users may change their password by selecting My Account/Profile.</li>
<li>For subsequent logins, use the password of record.</li>
<li>Once logged in, users will see CE Certificates already granted. A second screen provides a list of searches that earned 0.5 credits. Users may select some or all of these searches for a new Certificate, up to a maximum of 40 searches (20 credits) for a single Certificate.</li>
<li>Users are required to complete a brief evaluation of the Internet point of care CE learning format prior to receiving the first Certificate.</li>
<li><u>Credits must be claimed within six months of the search date</u>. Searches and credits that remain unclaimed are deleted after six months.</li>
<li>Certificates will remain online in the Credit Tracker for a six-year period.</li> 
</ul>
