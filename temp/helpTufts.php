<h2><a name="earn">How to earn CE Credit/Contact Hours</a></h2>
<h3>After finishing a qualified search, you may complete the additional required steps to earn CE for that activity (0.5 credits per search). These steps include answering questions in order to document 1) the condition or clinical issue (what you needed to find), 2) the contents located (what you found), and 3) the result of the search (what you learned). Follow the instructions for each question.</h3>
<h3>You then enter your name and email address, and select the type of credit. Once the information is submitted, you will receive an email with instructions for claiming DynaMed credits on the Tufts Online Credit Tracker.</h3>
<h3><a href="#top">back to top</a></h3>
